create or replace function public.add_points(
  target_user uuid,
  point_delta integer,
  xp_delta integer,
  reason_text text,
  ref_text text default ''
)
returns public.user_wallets
language plpgsql
security definer
set search_path = public
as $$
declare
  safe_points integer;
  safe_xp integer;
  updated_wallet public.user_wallets;
begin
  safe_points := greatest(least(coalesce(point_delta, 0), 10000), -100000);
  safe_xp := greatest(least(coalesce(xp_delta, 0), 10000), -100000);

  perform public.ensure_wallet(target_user);

  update public.user_wallets
  set
    points = greatest(0, points + safe_points),
    total_points = total_points + greatest(safe_points, 0),
    xp = greatest(0, xp + safe_xp),
    level = greatest(1, floor(sqrt(greatest(0, xp + safe_xp)::double precision / 120.0))::integer + 1),
    updated_at = now()
  where user_id = target_user
  returning * into updated_wallet;

  insert into public.point_ledger (user_id, amount, xp_amount, reason, ref_id)
  values (target_user, safe_points, safe_xp, coalesce(reason_text, 'points'), coalesce(ref_text, ''));

  return updated_wallet;
end;
$$;

