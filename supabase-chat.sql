  end if;

  if item.item_type not in ('name_style', 'avatar_frame', 'theme', 'badge', 'title', 'avatar_color', 'game_skin') then
    raise exception 'item_not_equippable';
  end if;

  slot_value := coalesce(nullif(item.equip_slot, ''), item.item_type);

  update public.user_inventory ui
  set equipped = false
  from public.shop_items si
  where ui.user_id = auth.uid()
    and ui.item_key = si.item_key
    and coalesce(nullif(si.equip_slot, ''), si.item_type) = slot_value;

  update public.user_inventory
  set equipped = true
  where user_id = auth.uid()
    and item_key = item.item_key;

  if item.item_type = 'name_style' then
    style_value := coalesce(item.payload->>'name_style', 'solid');
    if style_value not in ('solid', 'gradient', 'rainbow') then
      style_value := 'solid';
    end if;

    color_a := coalesce(item.payload->>'name_color_a', '#39ff88');
    color_b := coalesce(item.payload->>'name_color_b', '#ffdc5e');

    if color_a !~ '^#[0-9A-Fa-f]{6}$' then
      color_a := '#39ff88';
    end if;

    if color_b !~ '^#[0-9A-Fa-f]{6}$' then
      color_b := '#ffdc5e';
    end if;

    update public.profiles
    set name_style = style_value,
        name_color_a = color_a,
        name_color_b = color_b,
        updated_at = now()
    where id = auth.uid()
    returning * into profile;
  elsif item.item_type = 'avatar_frame' then
    frame_value := coalesce(item.payload->>'frame', 'none');
    if frame_value not in ('none', 'neon', 'gold', 'emerald', 'ruby', 'ice', 'shadow', 'arcade', 'prismatic') then
      frame_value := 'none';
    end if;

    update public.profiles
    set avatar_frame = frame_value,
        updated_at = now()
    where id = auth.uid()
    returning * into profile;
  elsif item.item_type = 'theme' then
    theme_value := coalesce(item.payload->>'theme', 'default');
    if theme_value not in ('default', 'blue', 'gold', 'midnight', 'forest', 'synthwave', 'void', 'candy') then
      theme_value := 'default';
    end if;

    update public.profiles
    set profile_theme = theme_value,
        updated_at = now()
    where id = auth.uid()
    returning * into profile;
  elsif item.item_type = 'badge' then
    badge_value := left(coalesce(item.payload->>'badge', ''), 16);

    update public.profiles
    set active_badge = badge_value,
        updated_at = now()
    where id = auth.uid()
    returning * into profile;
  elsif item.item_type = 'title' then
    title_value := left(coalesce(item.payload->>'title', 'Nouveau joueur'), 32);

    update public.profiles
    set title = title_value,
        updated_at = now()
    where id = auth.uid()
    returning * into profile;
  elsif item.item_type = 'avatar_color' then
    color_a := coalesce(item.payload->>'avatar_color', '#39ff88');
    if color_a !~ '^#[0-9A-Fa-f]{6}$' then
      color_a := '#39ff88';
    end if;

    update public.profiles
    set avatar_color = color_a,
        updated_at = now()
    where id = auth.uid()
    returning * into profile;
  else
    select *
    into profile
    from public.profiles
    where id = auth.uid();
  end if;

  return profile;
end;
$$;

create or replace function public.complete_solo_game(game_key_input text, score_input integer)
returns public.user_wallets
language plpgsql
security definer
set search_path = public
as $$
declare
  safe_game text;
  safe_score integer;
  reward integer;
  wallet public.user_wallets;
  settings public.game_settings;
begin
  safe_game := lower(trim(coalesce(game_key_input, '')));

  select *
  into settings
  from public.game_settings
  where game_key = safe_game;

  if settings.game_key is null or settings.enabled = false then
    raise exception 'invalid_game';
  end if;

  if safe_game not in ('reflex', 'memory', 'runner', 'aim', 'cipher', 'snake', 'puzzle', 'rpg', 'dungeon', 'tycoon', 'space', 'platformer') then
    raise exception 'invalid_game';
  end if;

  safe_score := greatest(0, least(coalesce(score_input, 0), settings.score_cap));

  if exists (
    select 1
    from public.game_scores gs
    where gs.user_id = auth.uid()
      and gs.game_key = safe_game
      and gs.created_at > now() - make_interval(secs => settings.cooldown_seconds)
  ) then
    raise exception 'game_cooldown';
  end if;

  reward := case safe_game
    when 'reflex' then floor(safe_score / 90.0)::integer
    when 'memory' then floor(safe_score / 80.0)::integer
    when 'runner' then floor(safe_score / 55.0)::integer
    when 'aim' then floor(safe_score / 70.0)::integer
    when 'cipher' then floor(safe_score / 80.0)::integer
    when 'snake' then floor(safe_score / 80.0)::integer
    when 'puzzle' then floor(safe_score / 95.0)::integer
    when 'rpg' then floor(safe_score / 90.0)::integer
    when 'dungeon' then floor(safe_score / 90.0)::integer
    when 'tycoon' then floor(safe_score / 150.0)::integer
    when 'space' then floor(safe_score / 85.0)::integer
    when 'platformer' then floor(safe_score / 85.0)::integer
    else settings.reward_min
  end;

  reward := least(settings.reward_max, greatest(settings.reward_min, reward));

  insert into public.game_scores (user_id, game_key, score, reward_points)
  values (auth.uid(), safe_game, safe_score, reward);

  wallet := public.add_points(auth.uid(), reward, reward * 2, 'game:' || safe_game, safe_score::text);
  return wallet;
end;
$$;

