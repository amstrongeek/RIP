create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  requested_pseudo text;
begin
  requested_pseudo := nullif(trim(new.raw_user_meta_data->>'pseudo'), '');

  insert into public.profiles (id, pseudo, email)
  values (
    new.id,
    coalesce(requested_pseudo, 'Player_' || left(new.id::text, 8)),
    coalesce(new.email, '')
  )
  on conflict (id) do update
  set
    pseudo = excluded.pseudo,
    email = excluded.email,
    last_seen = now(),
    updated_at = now();

  insert into public.room_members (room_id, user_id, role)
  values ('00000000-0000-4000-8000-000000000001', new.id, 'member')
  on conflict do nothing;

  insert into public.user_wallets (user_id)
  values (new.id)
  on conflict do nothing;

  return new;
end;
$$;

