create or replace function public.room_is_public(target_room uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.chat_rooms cr
    where cr.id = target_room
      and cr.kind = 'public'
  );
$$;

create or replace function public.current_user_is_room_member(target_room uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.room_members rm
    where rm.room_id = target_room
      and rm.user_id = auth.uid()
  );
$$;

create or replace function public.current_user_is_room_owner(target_room uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.chat_rooms cr
    where cr.id = target_room
      and cr.owner_id = auth.uid()
  );
$$;

create or replace function public.current_user_can_read_room(target_room uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select public.room_is_public(target_room)
    or public.current_user_is_room_member(target_room)
    or public.current_user_is_room_owner(target_room);
$$;

