create table if not exists public.chat_rooms (
  id uuid primary key default gen_random_uuid(),
  name text not null check (char_length(name) between 2 and 40),
  kind text not null default 'public' check (kind in ('public', 'private', 'dm')),
  owner_id uuid references auth.users(id) on delete set null,
  invite_code text unique not null default lower(substr(replace(gen_random_uuid()::text, '-', ''), 1, 8)),
  created_at timestamptz not null default now()
);

create table if not exists public.room_members (
  room_id uuid not null references public.chat_rooms(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  role text not null default 'member' check (role in ('owner', 'member')),
  created_at timestamptz not null default now(),
  primary key (room_id, user_id)
);

create table if not exists public.chat_messages (
  id bigint generated by default as identity primary key,
  room_id uuid references public.chat_rooms(id) on delete cascade,
  user_id text not null,
  pseudo text not null check (char_length(pseudo) between 3 and 20),
  content text not null check (char_length(content) between 1 and 500),
  created_at timestamptz not null default now()
);

