  title_input text,
  body_input text,
  page_url_input text default '',
  user_agent_input text default ''
)
returns public.bug_reports
language plpgsql
security definer
set search_path = public
as $$
declare
  clean_title text;
  clean_body text;
  clean_url text;
  clean_agent text;
  clean_key text;
