create or replace function public.room_is_public(target_room uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.chat_rooms cr
    where cr.id = target_room
      and cr.kind = 'public'
  );
$$;

create or replace function public.current_user_is_room_member(target_room uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
