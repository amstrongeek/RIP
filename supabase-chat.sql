create or replace function public.complete_solo_game(game_key_input text, score_input integer)
returns public.user_wallets
language plpgsql
security definer
set search_path = public
as $$
declare
  safe_game text;
  safe_score integer;
  reward integer;
  wallet public.user_wallets;
begin
  safe_game := lower(trim(coalesce(game_key_input, '')));
  safe_score := greatest(0, least(coalesce(score_input, 0), 100000));

  if safe_game not in ('reflex', 'memory', 'runner', 'aim', 'cipher') then
    raise exception 'invalid_game';
  end if;

  if exists (
    select 1
    from public.game_scores gs
    where gs.user_id = auth.uid()
      and gs.game_key = safe_game
      and gs.created_at > now() - interval '12 seconds'
  ) then
    raise exception 'game_cooldown';
  end if;

  reward := case safe_game
    when 'reflex' then least(90, greatest(8, floor(safe_score / 90.0)::integer))
    when 'memory' then least(120, greatest(10, floor(safe_score / 80.0)::integer))
    when 'runner' then least(140, greatest(10, floor(safe_score / 55.0)::integer))
    when 'aim' then least(130, greatest(10, floor(safe_score / 70.0)::integer))
    when 'cipher' then least(120, greatest(10, floor(safe_score / 80.0)::integer))
    else 10
  end;

  insert into public.game_scores (user_id, game_key, score, reward_points)
  values (auth.uid(), safe_game, safe_score, reward);

  wallet := public.add_points(auth.uid(), reward, reward * 2, 'game:' || safe_game, safe_score::text);
  return wallet;
end;
$$;

