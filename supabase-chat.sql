create or replace function public.submit_duel_choice(duel_id uuid, choice_text text)
returns public.game_duels
language plpgsql
security definer
set search_path = public
as $$
declare
  duel public.game_duels;
  clean_choice text;
  winner uuid;
begin
  clean_choice := lower(trim(choice_text));

  if clean_choice not in ('rock', 'paper', 'scissors') then
    raise exception 'invalid_choice';
  end if;

  select *
  into duel
  from public.game_duels
  where id = duel_id
    and (host_id = auth.uid() or guest_id = auth.uid())
  for update;

  if duel.id is null then
    raise exception 'duel_not_found';
  end if;

  if duel.status = 'done' then
    return duel;
  end if;

  if duel.host_id = auth.uid() then
    update public.game_duels
    set host_choice = clean_choice,
        updated_at = now()
    where id = duel.id
    returning * into duel;
  elsif duel.guest_id = auth.uid() then
    update public.game_duels
    set guest_choice = clean_choice,
        updated_at = now()
    where id = duel.id
    returning * into duel;
  end if;

  if duel.host_choice is not null and duel.guest_choice is not null then
    winner := null;

    if duel.host_choice <> duel.guest_choice then
      if (duel.host_choice = 'rock' and duel.guest_choice = 'scissors')
        or (duel.host_choice = 'paper' and duel.guest_choice = 'rock')
        or (duel.host_choice = 'scissors' and duel.guest_choice = 'paper') then
        winner := duel.host_id;
      else
        winner := duel.guest_id;
      end if;
    end if;

    update public.game_duels
    set winner_id = winner,
        status = 'done',
        updated_at = now()
    where id = duel.id
    returning * into duel;

    if winner is null then
      perform public.add_points(duel.host_id, 8, 12, 'duel_draw', duel.id::text);
      perform public.add_points(duel.guest_id, 8, 12, 'duel_draw', duel.id::text);
    else
      perform public.add_points(winner, 35, 50, 'duel_win', duel.id::text);
      perform public.add_points(
        case when winner = duel.host_id then duel.guest_id else duel.host_id end,
        5,
        8,
        'duel_loss',
        duel.id::text
      );
    end if;
  end if;

  return duel;
end;
$$;

