create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

create or replace function public.join_room_by_code(room_code text)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  target_room uuid;
begin
  select id
  into target_room
  from public.chat_rooms
  where invite_code = lower(trim(room_code))
    and kind in ('private', 'public')
  limit 1;

  if target_room is null then
    raise exception 'room_not_found';
  end if;

  insert into public.room_members (room_id, user_id, role)
  values (target_room, auth.uid(), 'member')
  on conflict do nothing;

  return target_room;
end;
$$;

create or replace function public.create_or_get_dm(friend_id uuid)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  existing_room uuid;
  new_room uuid;
begin
  if friend_id = auth.uid() then
    raise exception 'invalid_friend';
  end if;

  if not exists (
    select 1
    from public.friend_requests fr
    where fr.status = 'accepted'
      and (
        (fr.sender_id = auth.uid() and fr.receiver_id = friend_id)
        or (fr.sender_id = friend_id and fr.receiver_id = auth.uid())
      )
  ) then
    raise exception 'not_friends';
  end if;

  select rm1.room_id
  into existing_room
  from public.room_members rm1
  join public.room_members rm2 on rm2.room_id = rm1.room_id
  join public.chat_rooms cr on cr.id = rm1.room_id
  where cr.kind = 'dm'
    and rm1.user_id = auth.uid()
    and rm2.user_id = friend_id
  limit 1;

  if existing_room is not null then
    return existing_room;
  end if;

  insert into public.chat_rooms (name, kind, owner_id)
  values ('DM', 'dm', auth.uid())
  returning id into new_room;

  insert into public.room_members (room_id, user_id, role)
  values
    (new_room, auth.uid(), 'owner'),
    (new_room, friend_id, 'member')
  on conflict do nothing;

  return new_room;
end;
$$;

