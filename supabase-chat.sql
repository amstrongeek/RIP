create or replace function public.update_my_profile(
  pseudo_input text,
  status_input text,
  bio_input text,
  website_input text default '',
  avatar_url_input text default ''
)
returns public.profiles
language plpgsql
security definer
set search_path = public
as $$
declare
  clean_pseudo text;
  clean_status text;
  clean_bio text;
  clean_website text;
  clean_avatar text;
  profile public.profiles;
begin
  clean_pseudo := trim(coalesce(pseudo_input, ''));
  clean_status := left(trim(coalesce(status_input, 'En ligne')), 32);
  clean_bio := left(trim(coalesce(bio_input, '')), 240);
  clean_website := left(trim(coalesce(website_input, '')), 120);
  clean_avatar := left(trim(coalesce(avatar_url_input, '')), 500);

  if clean_pseudo !~ '^[A-Za-z0-9_-]{3,20}$' then
    raise exception 'pseudo_invalid';
  end if;

  if char_length(clean_status) < 1 then
    clean_status := 'En ligne';
  end if;

  if clean_website <> '' and clean_website !~ '^https://[^[:space:]]+$' then
    raise exception 'website_invalid';
  end if;

  if clean_avatar <> '' and clean_avatar !~ '^https://[^[:space:]]+$' then
    raise exception 'avatar_invalid';
  end if;

  update public.profiles
  set pseudo = clean_pseudo,
      status = clean_status,
      bio = clean_bio,
      website = clean_website,
      avatar_url = case when clean_avatar <> '' then clean_avatar else avatar_url end,
      updated_at = now(),
      last_seen = now()
  where id = auth.uid()
  returning * into profile;

  if profile.id is null then
    raise exception 'profile_not_found';
  end if;

  return profile;
end;
$$;

